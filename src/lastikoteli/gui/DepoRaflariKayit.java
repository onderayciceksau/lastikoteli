    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String depo_adi = jComboBox1.getSelectedItem().toString();
        Integer kapasite = (Integer) jSpinner1.getValue();
        String rafno = jTextField2.getText();
        
        try {
            DepoRaflari dr = new DepoRaflari();
            dr.setRaf_adi(rafno);
            dr.setKapasite(kapasite);
            
            dr.setDepo(getDepolar().stream().filter((a) -> a.getDepo_adi().equalsIgnoreCase(depo_adi)).findAny().orElseThrow(new Supplier<Exception>() {
                @Override
                public Exception get() {
                    return new Exception("Depo Eşleşmedi.");
                }
            }));
            depoRaflariKayit(dr);
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(this, "Doğrulama sırasında hata oluştu",
                    "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

