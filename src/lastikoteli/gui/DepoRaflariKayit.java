    private void depoRaflariKayit(DepoRaflari dr) throws Exception {
        Connection con = DBConnection.getInstance().getConnection();
        PreparedStatement psmt = con.prepareStatement("insert into depo_raflari(raf_adi,depo_id,kapsite)values(?,?,?)");
        psmt.setString(1, dr.getRaf_adi());
        psmt.setInt(2, dr.getDepo().getId());
        psmt.setInt(3, dr.getKapasite());
        psmt.executeUpdate();
        psmt.close();
    }
    
    private List<Depolar> getDepolar() throws SQLException {
        List<Depolar> dList = new ArrayList<>();
        Connection con = DBConnection.getInstance().getConnection();
        PreparedStatement psmt = con.prepareStatement("select * from depolar");
        ResultSet rs = psmt.executeQuery();
        while (rs.next()) {
            Depolar d = new Depolar();
            d.setId(rs.getInt("id"));
            d.setDepo_adi(rs.getString("depo_adi"));
            d.setAdres(rs.getString("adres"));
            dList.add(d);
        }
        rs.close();
        psmt.close();
        return dList;
    }

